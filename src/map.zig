pub fn isSolid(x: i32, y: i32) bool {
    if (x < 0 or y < 0) return true;
    const ux: usize = @intCast(x);
    const uy: usize = @intCast(y);
    if (ux >= WIDTH or uy >= HEIGHT) return true;
    return world[uy][ux] != 0;
}

pub fn wallColor(cell: u8) u32 {
    return switch (cell) {
        1 => 0xFF_CC2222,
        2 => 0xFF_22CC22,
        3 => 0xFF_2222CC,
        4 => 0xFF_CCCCCC,
        else => 0xFF_888888,
    };
}
